#!/usr/bin/perl

# 負の値の添字の挙動.
@ary = ("ABC", "DEF", "GHI", "JKL");
print "$ary[0]\n"; # 添字が0.(一般的な添字.)
print "$ary[-1]\n"; # 添字が-1.(負の値の添字.)
print "$ary[-2]\n"; # 添字が-2.(負の値の添字.)
print "$ary[-3]\n"; # 添字が-3.(負の値の添字.)
print "$ary[-4]\n"; # 添字が-4.(負の値の添字.)
print "$ary[-5]\n"; # 添字が-5.(負の値の添字2周目.)

# 範囲を超える値の添字の挙動.
print "$ary[3]\n"; # 添字が3.(一般的な添字.)
print "$ary[4]\n"; # 添字が4.(範囲を超える値の添字.)
