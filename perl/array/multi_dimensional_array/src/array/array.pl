#!/usr/bin/perl

# 多次元配列の生成.
@multi = ([1, 2, 3],
          [4, 5, 6],
          [7, 8, 9]); # 無名配列リファレンスの配列とすることで多次元配列を生成.

# 要素の参照.
print $multi[0]->[0]."\n"; # 0行0列なので1.
print $multi[1]->[1]."\n"; # 1行1列なので5.
print $multi[2]->[2]."\n"; # 2行2列なので9.

# アロー演算子の省略.
print $multi[0][0]."\n";
print $multi[1][1]."\n";
print $multi[2][2]."\n";
