#!/usr/bin/perl

# 範囲演算子で変数リストの要素である変数に値を代入.
($a, $b, $c) = (1..3); # $a = 1, $b = 2, $c = 3となる.
print '$a = '.$a."\n";
print '$b = '.$b."\n";
print '$c = '.$c."\n";

# 範囲演算子で配列の要素に値を代入.
@ary = (1, 3..5, 7); # @aryは(1, 3, 4, 5, 7)で構成された配列.
foreach $var (@ary){
  print '$var = '.$var."\n";
}

# for文も範囲演算子で表現.
for (1..3){ # $_にループ変数の値として代入される.
  print $_."\n";
}

