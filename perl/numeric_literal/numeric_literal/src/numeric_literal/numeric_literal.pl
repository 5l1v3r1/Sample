#!/usr/bin/perl

#さまざまな数値リテラル.
$num1 = 123; # 整数値123
$num2 = 0.12; # 小数値0.12
$num3 = 0x2f; # 16進数値0x2f(10進数では47.)
$num4 = 0123; # 8進数0123(10進数では83.)
$num5 = 2E3; # a * 10のn乗.(2 * 10の3乗なので2000.) 
$num6 = 1_000_000_000; # アンダースコア区切り(3桁区切りなどができる.)

#出力.
print "$num1\n"; # $num1の出力.
print "$num2\n"; # $num2の出力.
print "$num3\n"; # $num3の出力.
print "$num4\n"; # $num4の出力.
print "$num5\n"; # $num5の出力.
print "$num6\n"; # $num6の出力.
