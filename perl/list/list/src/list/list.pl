#!/usr/bin/perl

# リストで変数を初期化.
($x, $y, $z) = (10, 20, 30); # $x = 10, $y = 20, $z = 30で初期化.
print '$x = '."$x".', $y = '."$y".', $z = '."$z\n"; # $x, $y, $zを出力.

# リストで配列を初期化.
@array = (100, 200, 300); # 100, 200, 300という3要素の配列を初期化.
for ($i = 0; $i < 3; $i++){ # 3回繰り返す.
  print '$array['.$i.'] = '."$array[$i]\n"; # $array[$i]を出力.
}
